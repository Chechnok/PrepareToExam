# Реалізувати функцію, яка виконує хешування повідомлення алгоритмом
# SHA-256. Програма повинна приймати на вхід рядок та виводити його геш-значення у
# шістнадцятковому форматі.

import hashlib


def sha256_hash(message: str) -> str:
    # Створення об'єкта хешу
    sha256 = hashlib.sha256()

    # Перетворення повідомлення у байти та хешування
    sha256.update(message.encode('utf-8'))

    # Повернення хеш-значення у шістнадцятковому форматі
    return sha256.hexdigest()


# Приклад використання
if __name__ == "__main__":
    message = input("Введіть рядок для хешування: ")
    hash_value = sha256_hash(message)
    print(f"Хеш-значення: {hash_value}")